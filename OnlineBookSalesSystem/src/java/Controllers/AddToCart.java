    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        DAO dao = new DAO();
        ArrayList<Product> listProduct = dao.getAllProduct();
        Cookie arr[] = request.getCookies();
        String txt = ""; 
        if (arr != null) {
            for (Cookie o : arr) {
                if (o.getName().equals("cart")) {
                    txt += o.getValue();
                    o.setMaxAge(0); 
                    response.addCookie(o);
                }
            }
        }
        String quantity = request.getParameter("quantity");
        String id = request.getParameter("addid");
        Product p = dao.getProductByID(Integer.parseInt(id));
        if(txt.isEmpty()) {
            txt = id + ":" + quantity;
        } else {
            txt = txt + "/" + id + ":" + quantity;
        }
        dao.updateProduct(Integer.parseInt(id), p.getQuantity() - Integer.parseInt(quantity));
        Cookie c = new Cookie("cart", txt);
        c.setMaxAge(60*60*24);
        response.addCookie(c);
        Cart cart = new Cart(txt, listProduct); 
        request.getSession().setAttribute("size", cart.getItems().size()); 
        request.setAttribute("product", p);
        request.getRequestDispatcher("View/detail.jsp").forward(request, response);
    }

